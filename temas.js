function cambiarTema(modo) {
    if (modo === 'dia') {
        document.body.style.background = '#fffefc';
    }
    if (modo === 'noche') {
        document.body.style.background = '#1c1b1b';
    }
    if (modo === 'daltonico') {
        document.body.style.background = '#f4f400';
    }
}
